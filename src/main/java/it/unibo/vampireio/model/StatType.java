package it.unibo.vampireio.model;

/**
 * Enum representing different types of stats in the game.
 */
enum StatType {
    /**
     * Represents the maximum health of a character.
     */
    MAX_HEALTH,
    /**
     * Represents the amount of damage mitigated.
     */
    ARMOR,
    /**
     * Represents the speed of projectiles.
     */
    SPEED,
    /**
     * Represents the character's movement speed.
     */
    MOVE_SPEED,
    /**
     * Represents the damage dealt by a character.
     */
    MIGHT,
    /**
     * Represents the cooldown time for weapons.
     */
    COOLDOWN,
}
